    private void jButtonApostarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonApostarMouseClicked

        if (!(jTextFieldApuestaLocal.getText().equalsIgnoreCase("")) && (jTextFieldApuestaLocal.getText().equalsIgnoreCase("0") || jTextFieldApuestaLocal.getText().equalsIgnoreCase("1") || jTextFieldApuestaLocal.getText().equalsIgnoreCase("2") || jTextFieldApuestaLocal.getText().equalsIgnoreCase("3") || jTextFieldApuestaLocal.getText().equalsIgnoreCase("4") || jTextFieldApuestaLocal.getText().equalsIgnoreCase("5"))) {
            if (!(jTextFieldApuestaVisitante.getText().equalsIgnoreCase("")) && (jTextFieldApuestaVisitante.getText().equalsIgnoreCase("0") || jTextFieldApuestaVisitante.getText().equalsIgnoreCase("1") || jTextFieldApuestaVisitante.getText().equalsIgnoreCase("2") || jTextFieldApuestaVisitante.getText().equalsIgnoreCase("3") || jTextFieldApuestaVisitante.getText().equalsIgnoreCase("4") || jTextFieldApuestaVisitante.getText().equalsIgnoreCase("5"))) {
                Apuesta a = new Apuesta(jTextFieldNombre.getText(), jTextFieldApuestaLocal.getText(), jTextFieldApuestaVisitante.getText(), jCheckBoxPagado.isSelected());
                vPorra.add(a);
                JOptionPane.showMessageDialog(this, "Apuesta realizada correctamente");
            } else {
                JOptionPane.showMessageDialog(this, "Haz una apuesta real, maximo 5 goles");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Haz una apuesta real, maximo 5 goles");
        }

        Iterator it = vPorra.iterator();
        Apuesta ap;
        while (it.hasNext()) {
            ap = (Apuesta) it.next();
            jTextAreaApuestas.setText(vPorra.toString());
        }

        jTextFieldApuestaLocal.setText("");
        jTextFieldApuestaVisitante.setText("");
        jTextFieldNombre.setText("");


    }//GEN-LAST:event_jButtonApostarMouseClicked

