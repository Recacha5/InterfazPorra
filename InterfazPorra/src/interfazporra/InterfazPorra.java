    private void jButtonComprobarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonComprobarMouseClicked

        int local = (int) (Math.random() * 6);
        int visitante = (int) (Math.random() * 6);
        double dineroGanado = 0;
        //int local = 2;
        //int visitante = 1;
        LinkedList<Apuesta> vGanadores = new LinkedList();
        jLabelGolLocal.setText("" + local);
        jLabelGolVisitante.setText("" + visitante);
        //jButtonComprobar.setEnabled(false);

        Iterator it = vPorra.iterator();
        Apuesta ap;

        while (it.hasNext()) {
            ap = (Apuesta) it.next();
            if (ap.getAPLocal().equalsIgnoreCase(jLabelGolLocal.getText()) && ap.getAPVisitante().equalsIgnoreCase(jLabelGolVisitante.getText())) {
                vGanadores.add(ap);
            }
        }
        if (!vGanadores.isEmpty()) {
            dineroGanado = ((5 * vPorra.size()) / vGanadores.size());
            JOptionPane.showMessageDialog(this, "A continuaciÃ³n se muestran sÃ³lo los ganadores");
        } else {
            JOptionPane.showMessageDialog(this, "Gana la banca");
        }

        Iterator it2 = vGanadores.iterator();
        Apuesta win;
        while (it2.hasNext()) {
            win = (Apuesta) it2.next();
            if (win.isPagado()) {
                jTextAreaApuestas.setText(vGanadores.toString() + dineroGanado + "â¬");
            }else{
                jTextAreaApuestas.setText(vGanadores.toString() + dineroGanado + "â¬" + " Falta Pagar");
            }
        }


    }//GEN-LAST:event_jButtonComprobarMouseClicked

